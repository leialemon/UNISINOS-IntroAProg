# Exercício 12. Comparando os comandos de repetição for e while, em quais ocasiões é mais comum a utilização de um ou de outro?
# O comando while geralmente é mais utilizado quando não sabemos a quantidade de repetições que nosso código fará. Por exemplo, “enquanto o usuário não acertar o usuário e a senha, peça novamente”. 
# Já o comando for é mais utilizado quando sabemos o número de iterações a realizar. Por exemplo, “o usuário possui 5 tentativas para acertar o usuário e a senha”.

