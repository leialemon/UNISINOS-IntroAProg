public class Cor{
	public int r;
	public int g;
	public int b;

	public Cor(int r, int g, int b){
		this.r = r;
		this.g = g;
		this.b = b;
	}

	public void setR(int r){
		this.r = r;
	}
	
	public int getR(){
		return this.r;
	}

	public void setG(int g){
		this.g = g;
	}
	
	public int getG(){
		return this.g;
	}

	public void setB(int b){
		this.b = b;
	}

	public int getB(){
		return this.b;
	}
}
